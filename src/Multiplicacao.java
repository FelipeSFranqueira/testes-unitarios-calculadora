/**
 * Uma classe simples que realiza a operação de multiplicação entre dois valores.
 *
 * @author Felipe Sanches Franqueira
 * @version 1.0
 * @since Release 1.0 da aplicação
 */
public class Multiplicacao {
    /**
     * Método que realiza uma operação de multiplicação.
     *
     * @param a O primeiro operando.
     * @param b O segundo operando.
     * @return O resultado da multiplicação.
     */
    public int multiplicar (int a, int b) {
        return a * b;
    }
}
