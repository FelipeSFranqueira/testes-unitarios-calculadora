/**
 * Uma classe simples que realiza a operação de subtração entre dois valores.
 *
 * @author Felipe Sanches Franqueira
 * @version 1.0
 * @since Release 1.0 da aplicação
 */
public class Subtracao {
    /**
     * Método que realiza uma operação de subtração.
     *
     * @param a O operando do qual será subtraído.
     * @param b O operando a ser subtraído.
     * @return O resultado da subtração.
     */
    public int subtrair (int a, int b) {
        return a - b;
    }
}
