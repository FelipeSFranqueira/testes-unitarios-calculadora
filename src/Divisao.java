/**
 * Uma classe simples que realiza a operação de divisão entre dois valores.
 *
 * @author Felipe Sanches Franqueira
 * @version 1.0
 * @since Release 1.0 da aplicação
 */
public class Divisao {
    /**
     * Método que realiza uma operação de divisão.
     *
     * @param a O operando que será dividido.
     * @param b O operando que irá dividir.
     * @return O resultado da divisão.
     */
    public int dividir (int a, int b) {
        return a / b;
    }
}
