/**
 * Uma classe simples que realiza a operação de adição entre dois valores.
 *
 * @author Felipe Sanches Franqueira
 * @version 1.0
 * @since Release 1.0 da aplicação
 */
public class Soma {
    /**
     * Método que realiza uma operação de adição.
     *
     * @param a O primeiro operando.
     * @param b O segundo operando.
     * @return O resultado a adição.
     */
    public int somar(int a, int b) {
        return a + b;
    }
}


